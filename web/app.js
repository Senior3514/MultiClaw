const SETTINGS_KEY = 'multiclaw-settings';
let currentStep = 1;
const totalSteps = 3;

function loadSettings() {
  try {
    return JSON.parse(localStorage.getItem(SETTINGS_KEY) || '{}');
  } catch {
    return {};
  }
}

function renderBrand(data) {
  const rows = [
    ['Project', data.projectName],
    ['Company ID', data.companyId],
    ['Product origin', data.productOrigin],
    ['Autonomy mode', data.autonomyMode],
    ['Positioning', `${data.archetype} generated by MultiClaw`],
    ['Audience', data.audience],
    ['Business model', data.businessModel],
    ['Tone', data.tone],
  ];

  return rows.map(([label, value]) => `
    <div class="brand-row">
      <small>${label}</small>
      <strong>${value}</strong>
      <p>${label === 'Company ID' ? 'Safe public identifier for the generated company, without exposing server internals.' : 'Generated from the founder intake and used as operating context.'}</p>
    </div>
  `).join('');
}

function renderRouting(profile) {
  return Object.entries(profile).map(([label, value]) => `
    <div class="route-card">
      <small>${label}</small>
      <strong>${value}</strong>
      <p>Selected as the current default routing posture for this capability.</p>
    </div>
  `).join('');
}

function renderOrg(roles) {
  return roles.map((role) => `
    <div class="role-card">
      <h4>${role.title}</h4>
      <p>${role.scope}</p>
    </div>
  `).join('');
}

function renderMissions(missions) {
  return missions.map((item, index) => `
    <div class="mission-card">
      <small>Mission ${index + 1}</small>
      <strong>${item}</strong>
      <p>Immediate operating focus for the first company cycle.</p>
    </div>
  `).join('');
}

function getFormData() {
  return {
    productOrigin: el('productOrigin').value,
    autonomyMode: el('autonomyMode').value,
    projectName: el('projectName').value.trim() || 'Untitled Project',
    description: el('description').value.trim(),
    audience: el('audience').value.trim(),
    businessModel: el('businessModel').value.trim(),
    stage: el('stage').value,
    topGoals: el('topGoals').value.trim(),
    tone: el('tone').value.trim(),
    roleTemplate: el('roleTemplate').value,
    customRoles: el('customRoles').value.trim(),
  };
}

function setStatus(message, kind = 'idle') {
  const status = el('generationStatus');
  status.textContent = message;
  status.className = `status-banner status-${kind}`;
}

function setCompanyLink(companyId) {
  const link = el('companyLink');
  const download = el('downloadPackLink');
  link.href = `./company.html?id=${encodeURIComponent(companyId)}`;
  link.classList.remove('hidden-link');
  download.href = `/api/company/${encodeURIComponent(companyId)}/download`;
  download.classList.remove('hidden-link');
}

function applyResult(data) {
  el('heroTitle').textContent = `${data.projectName}, powered by MultiClaw`;
  el('heroSubtitle').textContent = `${data.description} This company is designed as a ${data.archetype.toLowerCase()} with ${data.stage.toLowerCase()} urgency.`;
  el('departmentsCount').textContent = String(data.departmentsCount);
  el('rolesCount').textContent = String(data.rolesCount);
  el('brandCard').innerHTML = renderBrand(data);
  el('routingCard').innerHTML = renderRouting(data.routing);
  el('orgGrid').innerHTML = renderOrg(data.roles);
  el('missionBoard').innerHTML = renderMissions(data.missions);
  const stamp = new Date().toLocaleTimeString();
  setStatus(`Company generated successfully at ${stamp}. ID: ${data.companyId}`, 'success');
  setCompanyLink(data.companyId);
}

function applyPreset(name) {
  const preset = PRESETS[name];
  if (!preset) return;

  el('productOrigin').value = preset.productOrigin;
  el('autonomyMode').value = preset.autonomyMode;
  el('projectName').value = preset.projectName;
  el('description').value = preset.description;
  el('audience').value = preset.audience;
  el('businessModel').value = preset.businessModel;
  el('stage').value = preset.stage;
  el('topGoals').value = preset.topGoals;
  el('tone').value = preset.tone;
  if (preset.roleTemplate) el('roleTemplate').value = preset.roleTemplate;
  if (typeof preset.customRoles === 'string') el('customRoles').value = preset.customRoles;
  setStatus(`Preset loaded: ${preset.projectName}`, 'idle');
}

function renderWizard() {
  document.querySelectorAll('.wizard-step').forEach((step) => {
    step.classList.toggle('active', Number(step.dataset.step) === currentStep);
  });

  document.querySelectorAll('.wizard-dot').forEach((dot, index) => {
    dot.classList.toggle('active', index + 1 <= currentStep);
  });

  el('stepLabel').textContent = `Step ${currentStep} of ${totalSteps}`;
  el('backBtn').style.visibility = currentStep === 1 ? 'hidden' : 'visible';
  el('nextBtn').classList.toggle('hidden-link', currentStep === totalSteps);
  el('generateBtn').classList.toggle('hidden-link', currentStep !== totalSteps);
}

async function generateCompany() {
  const button = el('generateBtn');
  const original = button.textContent;
  button.textContent = 'Generating...';
  button.disabled = true;
  setStatus('Generating company...', 'working');

  try {
    const response = await fetch('/api/generate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(getFormData()),
      credentials: 'same-origin',
    });

    if (!response.ok) {
      const error = await response.json().catch(() => ({}));
      throw new Error(error.error || `Generation failed with status ${response.status}`);
    }

    const data = await response.json();
    applyResult(data);
  } catch (error) {
    console.error(error);
    setStatus(error.message || 'Generation failed. Check the server log.', 'error');
    alert(error.message || 'Generation failed. Check the server log.');
  } finally {
    button.textContent = original;
    button.disabled = false;
  }
}

el('nextBtn').addEventListener('click', () => {
  if (currentStep < totalSteps) {
    currentStep += 1;
    renderWizard();
  }
});

el('backBtn').addEventListener('click', () => {
  if (currentStep > 1) {
    currentStep -= 1;
    renderWizard();
  }
});

el('generateBtn').addEventListener('click', generateCompany);
document.querySelectorAll('[data-preset]').forEach((button) => {
  button.addEventListener('click', () => applyPreset(button.dataset.preset));
});

const savedSettings = loadSettings();
if (savedSettings.defaultProductOrigin) el('productOrigin').value = savedSettings.defaultProductOrigin;
if (savedSettings.defaultAutonomyMode) el('autonomyMode').value = savedSettings.defaultAutonomyMode;

renderWizard();
