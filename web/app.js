const el = (id) => document.getElementById(id);

function renderBrand(data) {
  const rows = [
    ['Project', data.projectName],
    ['Company ID', data.companyId],
    ['Positioning', `${data.archetype} generated by MultiClaw`],
    ['Audience', data.audience],
    ['Business model', data.businessModel],
    ['Tone', data.tone],
  ];

  return rows.map(([label, value]) => `
    <div class="brand-row">
      <small>${label}</small>
      <strong>${value}</strong>
      <p>${label === 'Company ID' ? 'Safe public identifier for the generated company, without exposing server internals.' : 'Generated from the founder intake and used as operating context.'}</p>
    </div>
  `).join('');
}

function renderRouting(profile) {
  return Object.entries(profile).map(([label, value]) => `
    <div class="route-card">
      <small>${label}</small>
      <strong>${value}</strong>
      <p>Selected as the current default routing posture for this capability.</p>
    </div>
  `).join('');
}

function renderOrg(roles) {
  return roles.map((role) => `
    <div class="role-card">
      <h4>${role.title}</h4>
      <p>${role.scope}</p>
    </div>
  `).join('');
}

function renderMissions(missions) {
  return missions.map((item, index) => `
    <div class="mission-card">
      <small>Mission ${index + 1}</small>
      <strong>${item}</strong>
      <p>Immediate operating focus for the first company cycle.</p>
    </div>
  `).join('');
}

function getFormData() {
  return {
    projectName: el('projectName').value.trim() || 'Untitled Project',
    description: el('description').value.trim(),
    audience: el('audience').value.trim(),
    businessModel: el('businessModel').value.trim(),
    stage: el('stage').value,
    topGoals: el('topGoals').value.trim(),
    tone: el('tone').value.trim(),
  };
}

function setStatus(message, kind = 'idle') {
  const status = el('generationStatus');
  status.textContent = message;
  status.className = `status-banner status-${kind}`;
}

function setCompanyLink(companyId) {
  const link = el('companyLink');
  link.href = `./company.html?id=${encodeURIComponent(companyId)}`;
  link.classList.remove('hidden-link');
}

function applyResult(data) {
  el('heroTitle').textContent = `${data.projectName}, powered by MultiClaw`;
  el('heroSubtitle').textContent = `${data.description} This company is designed as a ${data.archetype.toLowerCase()} with ${data.stage.toLowerCase()} urgency.`;
  el('departmentsCount').textContent = String(data.departmentsCount);
  el('rolesCount').textContent = String(data.rolesCount);
  el('brandCard').innerHTML = renderBrand(data);
  el('routingCard').innerHTML = renderRouting(data.routing);
  el('orgGrid').innerHTML = renderOrg(data.roles);
  el('missionBoard').innerHTML = renderMissions(data.missions);
  const stamp = new Date().toLocaleTimeString();
  setStatus(`Company generated successfully at ${stamp}. ID: ${data.companyId}`, 'success');
  setCompanyLink(data.companyId);
}

async function generateCompany() {
  const button = el('generateBtn');
  const original = button.textContent;
  button.textContent = 'Generating...';
  button.disabled = true;
  setStatus('Generating company...', 'working');

  try {
    const response = await fetch('/api/generate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(getFormData()),
    });

    if (!response.ok) {
      throw new Error(`Generation failed with status ${response.status}`);
    }

    const data = await response.json();
    applyResult(data);
  } catch (error) {
    console.error(error);
    setStatus('Generation failed. Check the server log.', 'error');
    alert('Generation failed. Check the server log.');
  } finally {
    button.textContent = original;
    button.disabled = false;
  }
}

el('generateBtn').addEventListener('click', generateCompany);
generateCompany();
